window.generateGenericTimeline = function(deadline){
  if(deadline === "通年" || deadline.includes("〜")){
    return [
      {d:"通年随時",p:"申請受付",n:""},
      {d:"申請後1-2ヶ月",p:"書類審査",n:""},
      {d:"審査後1ヶ月",p:"採択結果通知",n:""},
      {d:"採択後",p:"事業/研究開始",n:""},
      {d:"事業終了後",p:"実績報告書提出",n:""}
    ];
  }
  // 締切から推定
  const m = deadline.match(/(\d{4})-(\d{2})-(\d{2})/);
  if(!m) return [{d:deadline, p:"提出締切", n:""}];
  const [_, y, mo, da] = m;
  const base = new Date(parseInt(y), parseInt(mo)-1, parseInt(da));
  const addMonths = (n) => {const d = new Date(base); d.setMonth(d.getMonth()+n); return d.getFullYear()+"-"+String(d.getMonth()+1).padStart(2,'0')+"頃";};
  return [
    {d:deadline, p:"提出締切", n:""},
    {d:addMonths(2), p:"書類審査", n:"財団内部審査"},
    {d:addMonths(3), p:"採択結果通知", n:""},
    {d:addMonths(4), p:"事業/研究開始", n:""},
    {d:addMonths(13), p:"事業/研究終了", n:"単年度の場合"},
    {d:addMonths(15), p:"実績/成果報告書提出", n:""}
  ];
};
